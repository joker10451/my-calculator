import { describe, test, expect } from 'vitest';

describe('Financial Literacy Quiz - Data Tests', () => {
  test('should have exactly 10 questions', () => {
    const questions = [
      'Какой процент дохода рекомендуется откладывать на сбережения?',
      'Что такое подушка безопасности?',
      'Какая переплата по ипотеке 3 млн ₽ на 20 лет при ставке 22%?',
      'Что выгоднее при досрочном погашении кредита?',
      'Какой налог платит самозанятый при работе с физлицами?',
      'Что такое сложный процент?',
      'Сколько нужно накопить для финансовой независимости при расходах 50 000 ₽/мес?',
      'Что такое инфляция?',
      'Какое правило помогает контролировать расходы?',
      'Что такое НДФЛ?',
    ];
    expect(questions).toHaveLength(10);
  });

  test('each question should have exactly 4 options', () => {
    const options = [
      ['5%', '10-20%', '30-40%', '50%'],
      ['Страховка от болезней', 'Запас денег на 3-6 месяцев расходов', 'Кредитная карта с большим лимитом', 'Инвестиционный портфель'],
      ['Около 3 млн ₽', 'Около 6 млн ₽', 'Около 9 млн ₽', 'Около 12 млн ₽'],
      ['Уменьшить ежемесячный платёж', 'Уменьшить срок кредита', 'Одинаково', 'Зависит от банка'],
      ['13%', '6%', '4%', '15%'],
    ];
    options.forEach(opt => {
      expect(opt).toHaveLength(4);
    });
  });

  test('correct answer index should be valid for each question', () => {
    const correctAnswers = [1, 1, 2, 1, 2, 1, 2, 1, 0, 1];
    const options = [
      ['5%', '10-20%', '30-40%', '50%'],
      ['Страховка от болезней', 'Запас денег на 3-6 месяцев расходов', 'Кредитная карта с большим лимитом', 'Инвестиционный портфель'],
      ['Около 3 млн ₽', 'Около 6 млн ₽', 'Около 9 млн ₽', 'Около 12 млн ₽'],
      ['Уменьшить ежемесячный платёж', 'Уменьшить срок кредита', 'Одинаково', 'Зависит от банка'],
      ['13%', '6%', '4%', '15%'],
      ['Процент, который начисляется только на основную сумму', 'Процент, который начисляется на основную сумму и на накопленные проценты', 'Процент за просрочку платежа', 'Комиссия банка за обслуживание'],
      ['6 млн ₽', '12 млн ₽', '18 млн ₽', '24 млн ₽'],
      ['Рост зарплат', 'Снижение покупательной способности денег', 'Увеличение ключевой ставки ЦБ', 'Рост курса доллара'],
      ['Правило 50/30/20', 'Правило 72 часов', 'Правило 80/20', 'Правило 10 000 часов'],
      ['Налог на добавленную стоимость', 'Налог на доходы физических лиц', 'Налог на недвижимость', 'Налог на прибыль организаций'],
    ];

    correctAnswers.forEach((correct, i) => {
      expect(correct).toBeGreaterThanOrEqual(0);
      expect(correct).toBeLessThan(options[i].length);
    });
  });

  test('categories should cover all 5 areas', () => {
    const categories = ['Сбережения', 'Сбережения', 'Кредиты', 'Кредиты', 'Налоги', 'Инвестиции', 'Инвестиции', 'Основы', 'Основы', 'Налоги'];
    const uniqueCategories = [...new Set(categories)];
    expect(uniqueCategories).toHaveLength(5);
    expect(uniqueCategories).toContain('Сбережения');
    expect(uniqueCategories).toContain('Кредиты');
    expect(uniqueCategories).toContain('Налоги');
    expect(uniqueCategories).toContain('Инвестиции');
    expect(uniqueCategories).toContain('Основы');
  });
});
